			<h3 class="p3">Themes</h3>

			<p class="p3">Miyazaki's works are characterized by the recurrence of themes such as environmentalism, pacifism, feminism, love and family. His narratives are also notable for not pitting a hero against an unsympathetic antagonist.</p>



			<p class="p5">Miyazaki's films often emphasize environmentalism and the Earth's fragility. Margaret Talbot stated that Miyazaki dislikes modern technology, and believes much of modern culture is "thin and shallow and fake"; he anticipates a time with "no more high-rises". <em>Miyazaki felt frustrated</em> growing up in the Shōwa period from 1955–65 because "nature — the mountains and rivers — was being destroyed in the name of economic progress". Peter Schellhase of The Imaginative Conservative identified that several antagonists of Miyazaki's films "attempt to dominate nature in pursuit of political domination, and are ultimately destructive to both nature and human civilization". Miyazaki is critical of capitalism, globalization, and their effects on modern life. He believes that "a company is common property of the people that work there". Ram Prakash Dwivedi identified values of Mahatma Gandhi in the films of Miyazaki.</p>

