			<h3 class="p3">Creation process and influences</h3>
			<p class="p3">Miyazaki forgoes traditional screenplays in his productions, instead developing the film's narrative as he designs the storyboards. "We never know where the story will go but we just keep working on the film as it develops," he said. In each of his films, Miyazaki has employed traditional animation methods, drawing each frame by hand; computer-generated imagery has been employed in several of his later films, beginning with Princess Mononoke, to "enrich the visual look", though he ensures that each film can "retain the right ratio between working by hand and computer ... and still be able to call my films 2D". He oversees every frame of his films.</p>

			<p class="p3">Miyazaki has cited several Japanese artists as his influences, including Sanpei Shirato, Osamu Tezuka, Soji Yamakawa, and Isao Takahata. A number of Western authors have also influenced his works, including Frédéric Back, Lewis Carroll, Roald Dahl, Jean Giraud, Paul Grimault, Ursula K. Le Guin, and Yuri Norstein, as well as animation studio Aardman Animations (specifically the works of Nick Park). Specific works that have influenced Miyazaki include Animal Farm (1945), The Snow Queen (1957), and The King and the Mockingbird (1980). When animating young children, Miyazaki often <em>takes inspiration from his friends' children,</em> as well as memories of his own childhood.</p>


			<div class="p5">
				<p class="p5">Miyazaki has frequently been cited as an inspiration to numerous animators, directors and writers around the world, including Guillermo del Toro, Pete Docter, Glen Keane, John Lasseter, James Cameron, Steven Spielberg, Wes Anderson, Mamoru Hosoda, Dean DeBlois, Jennifer Lee, and Nick Park. Keane said Miyazaki is a "huge influence" on Walt Disney Animation Studios and has been "part of our heritage" ever since The Rescuers Down Under (1990). Artists from Pixar and Aardman Studios signed a tribute stating, "You're our inspiration, Miyazaki-san!" He has also been cited as inspiration for video game designers including Shigeru Miyamoto and Hironobu Sakaguchi, as well as the Avatar: The Last Airbender, and the video game Ori and the Blind Forest (2015).</p>
