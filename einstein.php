				<h3 class="p7">About</h3>

				<p class="p7">Near the beginning of his career, Einstein thought that Newtonian mechanics was no longer enough to reconcile the laws of classical mechanics with the laws of the electromagnetic field. This led him to develop his special theory of relativity during his time at the Swiss Patent Office in Bern (1902–1909). He subsequently realized that the principle of relativity could be extended to gravitational fields and published a paper on general relativity in 1916 introducing his theory of gravitation. He continued to deal with problems of statistical mechanics and quantum theory, which led to his explanations of particle theory and the motion of molecules. He also investigated the thermal properties of light and the quantum theory of radiation, the basis of laser, which laid the foundation of the photon theory of light. In 1917, he applied the <em>general theory of relativity</em> to model the structure of the universe.</p>

				<p class="p7">Einstein moved to Switzerland in 1895 and renounced his German citizenship in 1896. After being stateless for more than five years, he acquired Swiss citizenship in 1901, which he kept for the rest of his life. Except for one year in Prague, he lived in Switzerland between 1895 and 1914.</p>

				<p class="p7">He received his academic diploma from the Swiss federal polytechnic school in Zürich in 1900. Between 1902 and 1909 he was employed in Bern as a patent examiner at the Federal Office for Intellectual Property, the patent office. In 1905, called his annus mirabilis (miracle year), he published four groundbreaking papers, which attracted the attention of the academic world. That year, at the age of 26, he was awarded a Ph.D. by the University of Zurich.</p>

				<p class="p7">He taught theoretical physics for one year (1908/09) at the University of Bern, for two years (1909–11) at the University of Zurich, and after one year at the Charles University in Prague he returned to his alma mater ETH Zurich between 1912 and 1914, before he left for Berlin, where he was elected to the Prussian Academy of Sciences.</p>

				<p class="p7">In 1933, while Einstein was visiting the United States, Adolf Hitler came to power. Because of his Jewish background, Einstein did not return to Germany. He settled in the United States and became an American citizen in 1940. On the eve of World War II, he endorsed a letter to President Franklin D. Roosevelt alerting FDR to the potential development of <em>extremely powerful bombs of a new type</em> and recommending that the US begin similar research. This eventually led to the Manhattan Project. Einstein supported the Allies, but he generally denounced the idea of using nuclear fission as a weapon. He signed the Russell–Einstein Manifesto with British philosopher Bertrand Russell, which highlighted the danger of nuclear weapons. He was affiliated with the Institute for Advanced Study in Princeton, New Jersey, until his death in 1955.</p>

				<p class="p7">He published more than 300 scientific papers and more than 150 non-scientific works. His intellectual achievements and originality have made the word "Einstein" synonymous with "genius." Eugene Wigner compared him to his contemporaries, writing that <em>Einstein's understanding was deeper</em> even than Jancsi von Neumann's. His mind was both more penetrating and more original.</p>
