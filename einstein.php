				<h3 class="p3">Love of music</h3>


				<p class="p3">Einstein developed an appreciation for music at an early age. In his late journals he wrote: "If I were not a physicist, <em>I would probably be a musician.</em> I often think in music. I live my daydreams in music. I see my life in terms of music... I get most joy in life out of music."</p>


				<p class="p3">His mother played the piano reasonably well and wanted her son to learn the violin, not only to instill in him a love of music but also to help him assimilate into German culture. According to conductor Leon Botstein, Einstein began playing when he was 5. However, he did not enjoy it at that age.</p>

			</div>


			<p class="p5">When he turned 13, he discovered the violin sonatas of Mozart, whereupon he became enamored of Mozart's compositions and studied music more willingly. Einstein taught himself to play without "ever practicing systematically." He said that "love is a better teacher than a sense of duty." At age 17, he was heard by a school examiner in Aarau while playing Beethoven's violin sonatas. The examiner stated afterward that his playing was "remarkable and revealing of 'great insight'." What struck the examiner, writes Botstein, was that Einstein "displayed a deep love of the music, a quality that was and remains in short supply. Music possessed an unusual meaning for this student."</p>

