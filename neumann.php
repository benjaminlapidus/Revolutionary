			<div class="p5">
				<h3 class="p5">Ergodic Theory</h3>
				<p class="p5">In a series of papers published in 1932, von Neumann made foundational contributions to ergodic theory, a branch of mathematics that involves the states of dynamical systems with an invariant measure. Of the 1932 papers on ergodic theory, Paul Halmos writes that even "if von Neumann had never done anything else, they would have been sufficient to guarantee him mathematical immortality". By then von Neumann had already written his articles on operator theory, and the application of this work was instrumental in the von Neumann mean ergodic theorem.</p>	



				<h3 class="p5">Operator Theory</h3>
				<p class="p5"> Von Neumann introduced the study of rings of operators, through the von Neumann algebras. A von Neumann algebra is Algebra of bounded operators on a Hilbert  space that is closed in the weak operator topology and contains the identity operator. The von Neumann bicommutant theorem shows that the analytic definition  is equivalent to a purely algebraic definition as being equal to the bicommutant. Von Neumann embarked in 1936, with the partial collaboration of F.J. Murray,  on the general study of factors classification of von Neumann algebras. The six major papers in which he developed that theory between 1936 and 1940 "rank among the masterpieces of analysis in the twentieth century". The direct integral was later introduced in 1949 by John von Neumann.</p>

				<h3 class="p5">Contributions to Computing</h3>
				<p class="p5">Von Neumann was a founding figure in computing. Von Neumann was the inventor,in 1945, of the merge sort algorithm, in which the first and second halves of an array are each sorted recursively and then merged.  Von Neumann wrote the <em>23 pages long sorting program</em> for the EDVAC in ink. On the first page, traces of the phrase "TOP SECRET", which was written in pencil and later erased, can still be seen. He also worked on the philosophy of artificial intelligence with Alan Turing when the latter visited Princeton in the 1930s.</p>

				<p class="p5">Von Neumann's hydrogen bomb work was played out in the realm of computing, where he and StanisLaw Ulam developed simulations on von Neumann's digital computers for the hydrodynamic computations. During this time he contributed to the development of the Monte Carlo method, which allowed solutions to complicated problems to be approximated using random numbers. Flow chart from von Neumann's "Planning and coding of problems for an electronic computing instrument," published in 1947. Von Neumann's algorithm for simulating a fair coin with a biased coin is used in the "software whitening" stage of some hardware random number generators. Because using lists of "truly" random numbers was extremely slow, von Neumann develope a form of making pseudorandom numbers, using the middle-square method.  Though this method has been criticized as crude, von Neumann was aware of this: he justified it as being faster than any other method at his disposal, writing that "Anyone who considers arithmetical methods of producing random digits is, of course, in a state of sin." Von Neumann also noted that when this method went awry it did so obviously, unlike other methods which could be subtly incorrect.</p>		

				<h3 class="p5">Cognitive Abilities</h3>
				<p class="p5"> Nobel Laureate Hans Bethe said "I have sometimes wondered whether a brain like von Neumann's does not indicate a species superior to that of man", and later Bethe wrote that "[von Neumann's] brain indicated a new species, an evolution beyond man". Seeing von Neumann's mind at work, Eugene Wigner wrote, "one had the impression of a perfect instrument whose gears were machined to mesh accurately to a thousandth of an inch." Paul Halmos states that "von Neumann's speed was awe-inspiring." Israel Halperin said: <em>"Keeping up with him was ... impossible.</em> The feeling was you were on a tricycle chasing a racing car."  Edward Teller admitted that he "never could keep up with him". Teller also said "von Neumann would carry on a conversation with my 3-year-old son, and the two of them would talk as equals, and I sometimes wondered if he used the same principle when he talked to the rest of us." Peter Lax wrote "Von Neumann was addicted to thinking, and in particular to thinking about mathematics".</p>
