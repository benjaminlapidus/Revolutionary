				<h3 class="p2">Illness and Death</h3>

				<p class="p2">In 1955, von Neumann was diagnosed with what was either bone, pancreatic or prostate cancer. He was not able to accept the proximity of his own demise, and the shadow of impending death instilled great fear in him. He invited a Catholic priest, Father Anselm Strittmatter, O.S.B., to visit him for consultation. Von Neumann reportedly said, "So long as there is the possibility of eternal damnation for nonbelievers it is more logical to be a believer at the end", essentially saying that Pascal had a point, referring to Pascal's Wager. He had earlier confided to his mother, "There probably has to be a God. Many things are easier to explain if there is than if there isn't Father Strittmatter administered the last rites to him. Some of von Neumann's friends (such as Abraham Pais and Oskar Morgenstern) said they had always believed him to be "completely agnostic".</p>

				<p class="p2">Of this deathbed conversion, Morgenstern told Heims, "He was of course completely agnostic all his life, and then he suddenly turned Catholic—it doesn't agree with anything whatsoever in his attitude, outlook and thinking when he was healthy." Father Strittmatter recalled that even after his conversion, von Neumann did not receive much peace or comfort from it, as he still remained terrified of death. Von Neumann was on his deathbed when he entertained his brother by reciting by heart and word-for-word the first few lines of each page of Goethe's Faust.</p>

				<p class="p2">He died at age 53 on February 8, 1957, at the Walter Reed Army Medical Center in Washington, D.C., under military security lest he reveal military secrets while heavily medicated. He was buried at Princeton Cemetery in Princeton, Mercer County, New Jersey.</p>
